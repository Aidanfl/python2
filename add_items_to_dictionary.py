# This script demonstrates how to add new items to a dictionary.
# It starts with a dictionary containing three key-value pairs, then adds a new item ('calculator': 1).
# It also adds a numeric key-value pair (9: 18) to the dictionary, and both updates are printed.

# Save the pairs 'money':20, 'candy':3, 'pen': 3 to a variable d
# Dictionary
d = {'money': 20, 'candy': 3, 'pen': 3}
# Add a new item to the dictionary 'calculator' : 1
d['calculator'] = 1
print(d)
d[9] = 9 * 2
print(d)
