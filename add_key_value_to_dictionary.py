# This script demonstrates how to add a new key-value pair to an existing dictionary.
# It starts with a dictionary {0:10, 1:20} and then adds a new key-value pair (2:30).
# The updated dictionary is printed as the result.

d = {0:10, 1:20}
print("Sample Dictionary: ", d)
d[2] = 30
print("Expected Result: ", d)
