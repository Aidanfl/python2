# This script calculates the difference between two numerical expressions:
# - num1 is calculated as 4 + (2/7)
# - num2 is calculated as 6 + (5/9)
# The script finds the difference between num1 and num2, rounds the result to four decimal places, and prints it.

num1 = (4+(2/7))
num2 = (6+(5/9))

x = num1-num2
total = round(x, 4)

print(total)
