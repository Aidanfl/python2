# This script demonstrates how to concatenate two lists and sort the resulting list.
# It creates two lists: 'a' with values [2, 1, 6] and 'b' with values [3, 8, 5].
# The lists are concatenated using the '+' operator, sorted in ascending order, and the sorted list is printed.

a = [2, 1, 6]
b = [3, 8 ,5]

m = a + b
m.sort()
print(m)
