# This script demonstrates how to concatenate two lists in Python.
# It creates two lists: 'a' containing color names and 'b' containing a mix of a color and numbers.
# The lists are concatenated using the '+' operator and the result is printed.

a = ['red', 'yellow', 'blue']
b = ['green', 24, 98.6]

c = a + b
print(c)
