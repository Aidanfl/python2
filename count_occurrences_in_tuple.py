# This script counts the number of occurrences of a specific value in a tuple.
# It creates a tuple 'tup' with values (50, 10, 60, 70, 50).
# The count() method is used to count how many times the value 50 appears in the tuple.
# The result is printed.

tup = (50, 10, 60, 70, 50)
key = 50

count = tup.count(key)
print(count)
