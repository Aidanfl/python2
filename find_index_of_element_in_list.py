# This script demonstrates how to find the index of an element in a list.
# It creates a list 'answer' with the values ["Yes!", "No!", "Yes!", "No!", "Maybe."].
# It uses the index() method to find the position of the first occurrence of "No!" in the list and prints the index.

answer = ["Yes!", "No!", "Yes!", "No!", "Maybe."] 
num = answer.index("No!") 
print(num)
