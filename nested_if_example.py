# This script demonstrates the use of nested if statements.
# It checks if the variable x (set to 42) is greater than 1 and less than 100.
# - If x is greater than 1, it prints "More than one".
# - If x is also less than 100, it prints "Less than 100".

x = 42
if x > 1 :
    print('More than one')
    if x < 100 : 
        print('Less than 100') 
