# This script prints "Hello World" 300 times, along with the line numbers.
# It uses a while loop to iterate from 1 to 300, printing the line number followed by "Hello World".
# The iteration variable 'n' starts at 1 and increments by 1 until it reaches 300.

n = 1 # n is an iteration variable, 1 is the initial value

while n <= 300: # termination condition
    print(n, "Hello World")
    n = n + 1 # update statement
