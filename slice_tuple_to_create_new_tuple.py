# This script demonstrates how to create a new tuple by slicing an existing tuple.
# It starts with a tuple 'tup1' containing values (11, 22, 33, 44, 55, 66).
# A new tuple 'tup2' is created by slicing elements from index 3 to 5 (excluding 5) of 'tup1'.
# The new tuple 'tup2' is printed.

tup1 = (11, 22, 33, 44, 55, 66)

tup2 = tup1[3:5]

print("tuple2:", tup2)
