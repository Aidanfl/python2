# This script demonstrates how to assign values to variables and determine their data types.
# It assigns a number to a variable, creates a list with different types of elements, 
# and stores key-value pairs in a dictionary.
# The data type of the list and the dictionary is printed.

# Save a number 65 to a variable num
num = 65
# Save the elements 5, 8.9, 'k' to a variable m
m = [5, 8.9, 'k']
print(type(m))

# Save the pairs 'money':20, 'candy':3, 'pen': 3 to a variable d
# Dictionary
d = {'money': 20, 'candy':3, 'pen': 3}
print(type(d))
